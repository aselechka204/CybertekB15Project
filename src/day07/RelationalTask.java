package day07;

public class RelationalTask {
    public static void main(String[] args) {



        // Relational Operator >>= <<= == !=

        //Relation Operator is used to compare 2 numbers
        // and return true or false
        //anything action or expression
        //that return true or false result
        //can be called boolean expression

        int myNymber = 10;

        System.out.println(myNymber>5);
        System.out.println(myNymber<-19);
        System.out.println(myNymber>=5);
        System.out.println(myNymber>=8);
        System.out.println(myNymber<=5);
        System.out.println(myNymber>=8);

        // == is used to check for equality
        // and very different from single =

        System.out.println(myNymber==5);
        // != is used to check for inequality, in programming translate to NOT true
        System.out.println(myNymber!=10);
        System.out.println(myNymber!=6);

        // == and != are questions , checking for equality or inequality


    }
}
