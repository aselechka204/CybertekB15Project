package day10;

public class Calculator {
    public static void main(String[] args) {

        char operator = '-';


        switch (operator){
            case '+' :
                System.out.println(" you are about to add number");
                break ;
            case '-' :
                System.out.println(" you are about to subtract a number");
                break ;
            case '*' :
                System.out.println(" you are about to multiply number" );
                break ;
            case '/' :
                System.out.println(" you are about to divide number");
                break ;
            case 5 :
                System.out.println(" The selection is unknown" );

                break ;
            default:

        }



    }
}
